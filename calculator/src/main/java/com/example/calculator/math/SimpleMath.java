package com.example.calculator.math;

public class SimpleMath {

	public Double sum(Double firstName,Double secondName) {
		return firstName + secondName;
	}
	
	public Double subtraction(Double firstName,Double secondName) {
		return firstName - secondName;
	}
	
	public Double multiplication(Double firstName,Double secondName) {
		return firstName * secondName;
	}
	
	public Double division(Double firstName,Double secondName) {
		return firstName * secondName;
	}
	
}
